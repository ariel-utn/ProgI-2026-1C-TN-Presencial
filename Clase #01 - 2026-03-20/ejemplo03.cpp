#include <iostream>

using namespace std;

int main(){

    /// Las constantes se inicializan si o si en la declaración, después no se pueden cambiar
    /// Usamos la palabra reservada const
    /// Solemos escribir los nombres de las constantes en MAYUSCULAS
    const int NUMERO = 1; // Este valor queda fijo

    // Muestro la constante en pantalla
    cout << "El valor es: " << NUMERO;

    return 0;
}
